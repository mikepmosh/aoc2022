const parseInput = (rawInput) => rawInput;

const getSignal = (inputLines) => {
  const signal = [1];
  let registerValue = 1;

  for (const line of inputLines) {
    const [action, valueStr] = line.split(' ');

    signal.push(registerValue);
    if (action === "addx") {
      registerValue += parseInt(valueStr);
      signal.push(registerValue);
    }
  }

  return signal;
};

const part1 = (rawInput) => {
  const input = parseInput(rawInput);
  const inputLines = input.split('\n');

  const signal = getSignal(inputLines);

  let signalStrengths = 0;
  const pointsOfInterest = [20,60,100,140,180,220];
  for (const point of pointsOfInterest) {
    signalStrengths += point * signal[point-1];
  }

  return  signalStrengths;
};

const part2 = (rawInput) => {
  const input = parseInput(rawInput);
  const inputLines = input.split('\n');

  const signal = getSignal(inputLines);

  let screen = "";
  let drawPosition = 1;
  for (let clock=1; clock <= 240; clock++, drawPosition++) {
    const drawPixel = (signal[clock-1] <= drawPosition) && (drawPosition <= signal[clock-1] +2);
    screen += drawPixel ? '#' : '.';

    if (drawPosition == 40)
    {
      screen += '\n';
      drawPosition = 0;
    }
  }

  console.log(screen);

  return screen;
};

